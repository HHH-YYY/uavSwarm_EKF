function Traj=DataExtraction(Data,Dno)
% Each drone has 16 columns of data, where 1-3 are position, 4-6 are velocity, 7-9 are acceleration, 1
% 0-13 are quaternions, and 14-16 are angular velocity
% 每个无人机有16列数据，其中1-3为位置，4-6为速度，7-9为加速度，10-13为四元数，14-16为角速度
TrajPos=Data(:,16*Dno-15:16*Dno-13);%位置
TrajVel=Data(:,16*Dno-12:16*Dno-10);%速度
TrajAcc=Data(:,16*Dno-9:16*Dno-7);%加速度
TrajOrient=quaternion(Data(:,16*Dno-6:16*Dno-3));%四元数
TrajAngVel=Data(:,16*Dno-2:16*Dno);%角速度
Traj=struct(...
    'TrajPos',TrajPos,...
    'TrajVel',TrajVel,...
    'TrajAcc',TrajAcc,...
    'TrajOrient',TrajOrient,...
    'TrajAngVel',TrajAngVel);